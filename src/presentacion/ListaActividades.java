    public void crearListaActividades(List<DTActividad> actividades){
        
        DefaultTableModel modelo = new DefaultTableModel();

        // Asignar el modelo a la tabla
        jTable1.setModel(modelo);
        
        // Agregar columnas al modelo
        modelo.addColumn("Nombre");
        modelo.addColumn("Ciudad");
        modelo.addColumn("Costo");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Duracion");
        modelo.addColumn("Falta");
        modelo.addColumn("Departamento_Nombre");
        modelo.addColumn("Proveedor_Nickname");
        
        // Llenar la tabla con la lista de actividades
    for (DTActividad actividad : actividades) {
        Object[] fila = {actividad.getNombre(), actividad.getCiudad(), actividad.getCosto(), actividad.getDescripcion(), actividad.getDuracion(),
        actividad.getfAlta(),actividad.getDepartamento(),actividad.getProveedor()}; // Ajusta esto seg√∫n las propiedades de DTActividad
        
        
        modelo.addRow(fila);
    }
    
    }

